"""
A class containing a couple terminal colours that could be useful to indicate error or different kinds of data
    in the console.
"""


class TerminalColors:
    header = '\033[95m'
    ok_blue = '\033[94m'
    ok_green = '\033[92m'
    warning = '\033[93m'
    fail = '\033[91m'
    end_color = '\033[0m'
    bold = '\033[1m'
    underline = '\033[4m'
